package clase3estructurarepetitivafor;

public class Clase3EstructuraRepetitivaFor {

    public static void main(String[] args) {

        System.out.println("mostramos los numeros del 0 al diez ");
        for (int contador = 0; contador <= 10; contador++) {
            System.out.println(contador);
        }
        System.out.println("sali del for ");
        System.out.println("mostramos los numeros del 10 al 0");
        for (int x = 10; x >= 0; x--) {
            System.out.println(x);
        }
        System.out.println("sali del for ");
    }

}
